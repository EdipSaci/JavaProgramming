package day03_EscapeSequences;
/*
Escape Sequences : MUST be given with double quote
   \n : starts a new line
   \t : paragraph (tab)
   \\: single back slash
   \": double quote
 */


public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("Java\nPython \nC#");

        System.out.println("-----------------");

        System.out.println("Hello Cydeo\n How are you all today? \nIt is nice t seeyou all! \nwhat class do we next week?");

        System.out.println("---------------");

        System.out.println("\t Java is cool rogramming language");

        System.out.println("---------------------");

        System.out.println("/    \\"); //  in order to print one back slash we need to give two back slashes

        System.out.println("---------------------");

        System.out.println("my favorite Tv show \"Game of Thrones\"");



    }








}
